def translation(text):
    if text is None:
        print('is not null')
    pin_text_list = list(
        map(lambda x: pinyin(x, style=Style.NORMAL, heteronym=False)[0][0],
            text))
    pin_dict = dict(zip(pin_text_list, text))
    rd_list = list(
        map(lambda x: random.choice(sanskrit_dict.get(x, pin_dict.get(x))),
            pin_text_list))
    rd = (''.join(rd_list))
    return rd


